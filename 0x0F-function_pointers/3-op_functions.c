/**
 * op_add - adds 2 numbers.
 * @a: first number.
 * @b: second number.
 * Return: sum.
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtracts 2 numbers.
 * @a: first number.
 * @b: second number.
 * Return: difference.
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplies 2 numbers.
 * @a: first number.
 * @b: second number.
 * Return: product.
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divides 2 numbers.
 * @a: first number.
 * @b: second number.
 * Return: quotient.
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - finds the remainder when 2 numbers are divided.
 * @a: first number.
 * @b: second number.
 * Return: remainder.
 */
int op_mod(int a, int b)
{
	return (a % b);
}

